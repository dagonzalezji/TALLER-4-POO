package Punto5.Personas;

public class Persona {
    String nombre;
    String apellidos;
    String telefono;
    String direccion;
    public Persona(String nombre, String apellidos, String telefono, String direccion) {
        this.nombre = nombre;
        this.apellidos = apellidos;
        this.telefono = telefono;
        this.direccion = direccion;
    }
}